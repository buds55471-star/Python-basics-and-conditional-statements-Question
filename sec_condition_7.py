# Check if a triangle is valid given three sides.

side1 = int(input("Enter the 1st sides of triangle: "))
side2 = int(input("Enter the 2nd sides of triangle: "))
side3 = int(input("Enter the 3rd sides of triangle: "))

if side1==side2==side3:
    print("Triangle is given three sides")
else:
    print("Triangle is not given three sides")

